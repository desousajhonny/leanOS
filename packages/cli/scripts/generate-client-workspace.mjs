const scriptPath = fileURLToPath(import.meta.url);
const scriptDir = dirname(scriptPath);
const packageRoot = resolve(scriptDir, "..");
const projectRoot = resolve(packageRoot, "..", "..");
const examplesDir = resolve(projectRoot, "examples");
const clientWorkspaceDir = resolve(examplesDir, "client-workspace");
const treePath = resolve(examplesDir, "client-workspace-tree.md");

export async function generateClientWorkspace() {
  assertPathInside(projectRoot, examplesDir);
  assertPathInside(projectRoot, clientWorkspaceDir);
  assertPathInside(projectRoot, treePath);
  assertExactTarget(clientWorkspaceDir, resolve(projectRoot, "examples", "client-workspace"));

  const { createWorkspaceFiles } = await import(pathToFileURL(resolve(packageRoot, "dist", "templates", "workspace-template.js")).href);
  const files = createWorkspaceFiles(exampleAnswers);

  await mkdir(examplesDir, { recursive: true });
  await rm(clientWorkspaceDir, { recursive: true, force: true });
  await mkdir(clientWorkspaceDir, { recursive: true });

  for (const file of files) {
    const targetPath = resolve(clientWorkspaceDir, file.path);
    assertPathInside(clientWorkspaceDir, targetPath);
    await mkdir(dirname(targetPath), { recursive: true });
    await writeFile(targetPath, ensureTrailingNewline(file.content), "utf8");
  }

  await writeFile(treePath, ensureTrailingNewline(createTreeMarkdown(files)), "utf8");

  return {
    fileCount: files.length,
    workspaceDir: clientWorkspaceDir,
    treePath
  };
}

export function createTreeMarkdown(files) {
  const root = createTreeNode();

  for (const file of files) {
    const parts = file.path.split("/");
    let cursor = root;

    for (const directory of parts.slice(0, -1)) {
      if (!cursor.directories.has(directory)) {
        cursor.directories.set(directory, createTreeNode());
      }
      cursor = cursor.directories.get(directory);
    }

    cursor.files.push(parts.at(-1));
  }

  const treeLines = ["client-workspace/"];
  renderTree(root, "", treeLines);

  return [
    "# Client Workspace Tree",
    "",
    "Generated from the real LeanOS CLI workspace template.",
    "",
    "Do not edit `examples/client-workspace/` manually. Run `npm run generate:client-workspace` from the project root instead.",
    "",
    "```text",
    ...treeLines,
    "```"
  ].join("\n");
}

function createTreeNode() {
  return {
    directories: new Map(),
    files: []
  };
}

function renderTree(node, prefix, lines) {
  const entries = [
    ...[...node.directories.entries()]
      .sort(([left], [right]) => left.localeCompare(right))
      .map(([name, child]) => ({ name, child, type: "directory" })),
    ...node.files
      .sort((left, right) => left.localeCompare(right))
      .map((name) => ({ name, type: "file" }))
  ];

  entries.forEach((entry, index) => {
    const isLast = index === entries.length - 1;
    const connector = isLast ? "`-- " : "|-- ";

    if (entry.type === "directory") {
      lines.push(`${prefix}${connector}${entry.name}/`);
      renderTree(entry.child, `${prefix}${isLast ? "    " : "|   "}`, lines);
      return;
    }

    lines.push(`${prefix}${connector}${entry.name}`);
  });
}

function assertExactTarget(actualPath, expectedPath) {
  if (actualPath !== expectedPath) {
    throw new Error(`Refusing to regenerate unexpected path: ${actualPath}`);
  }
}

function assertPathInside(parentPath, childPath) {
  const childRelativePath = relative(parentPath, childPath);

  if (childRelativePath === "" || (!childRelativePath.startsWith("..") && !isAbsolute(childRelativePath))) {
    return;
  }

  throw new Error(`Refusing to write outside ${parentPath}: ${childPath}`);
}

function ensureTrailingNewline(content) {
  return content.endsWith("\n") ? content : `${content}\n`;
}

if (process.argv[1] && resolve(process.argv[1]) === scriptPath) {
  const result = await generateClientWorkspace();
  console.log(`Generated ${result.fileCount} files in ${relative(projectRoot, result.workspaceDir).split(sep).join("/")}`);
  console.log(`Generated ${relative(projectRoot, result.treePath).split(sep).join("/")}`);
}
